function one(name){
    var x = document.getElementById(name);

    if(x.style.display === "none"){
        x.style.display = "block";
    }else{
        x.style.display = "none";
    }


}

function openDraw(){
    var x = document.getElementById("drawBox");

    if(x.style.display === "none"){
        x.style.display = "block";
    }else{
        x.style.display = "none";
    }


}